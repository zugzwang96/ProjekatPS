    private void btnSacuvajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSacuvajActionPerformed
       Trotinet t = dajTrotinet();
       
       if(txtCena.getText().isEmpty() || txtModel.getText().isEmpty()){
           JOptionPane.showMessageDialog(this, "Greska prilikom izmene!\n" , "Izmena trotineta", JOptionPane.ERROR_MESSAGE);
           return;
       }
        
        try {
            Kontroler.getInstance().izmeniTrotinet(t);
            JOptionPane.showMessageDialog(this, "Trotinet uspesno izmenjen!\n", "Izmena trotineta", JOptionPane.INFORMATION_MESSAGE);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Greska prilikom izmene!\n" + ex.getMessage(), "Izmena trotineta", JOptionPane.ERROR_MESSAGE);
            Logger.getLogger(FrmKorisnikIzmena.class.getName()).log(Level.SEVERE, null, ex);

        }
    }//GEN-LAST:event_btnSacuvajActionPerformed


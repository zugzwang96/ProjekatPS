    void srediFormu(Trotinet izabraniTrotinet) {
        txtBrojSasije.setText(izabraniTrotinet.getBrojSasije());
        List<Marka> marke = Kontroler.getInstance().vratiSveMarke();
        cmbMarka.removeAllItems();
        
        for (Marka m : marke) {
            cmbMarka.addItem(m);
        }
        txtModel.setText(izabraniTrotinet.getModel());
        txtCena.setText(Integer.toString(izabraniTrotinet.getCena()));
        txtBrojSasije.setEditable(false);
    }
    
    private Trotinet dajTrotinet() {
        Trotinet trotinet = new Trotinet();
        trotinet.setBrojSasije(txtBrojSasije.getText().trim());
        trotinet.setModel(txtModel.getText().trim());
        int cena = Integer.parseInt(txtCena.getText().trim());
        trotinet.setCena(cena);
        trotinet.setZaposleni(getUlogovani());
        trotinet.setMarka((Marka) cmbMarka.getSelectedItem());
        return trotinet;
    }
